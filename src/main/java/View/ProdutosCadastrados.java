    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = jTableProdutosCadastrados.getSelectedRow();
        
        if(linhaSelecionada == -1){
            JOptionPane.showMessageDialog(null, "Você não selecionou nenhum Produto");
        }else{
            String nome = jTableProdutosCadastrados.getValueAt(linhaSelecionada, 1).toString();
            String descricao = jTableProdutosCadastrados.getValueAt(linhaSelecionada, 2).toString();
            String quantidade = jTableProdutosCadastrados.getValueAt(linhaSelecionada, 3).toString();
            String valorCompra = jTableProdutosCadastrados.getValueAt(linhaSelecionada, 4).toString();
            String valorVenda = jTableProdutosCadastrados.getValueAt(linhaSelecionada, 5).toString();
            
            EditarProduto editarProduto = new EditarProduto(nome, descricao, quantidade, valorCompra, valorVenda, controller);

            // Adicionando o WindowListener para chamar addLista ao fechar a janela
            editarProduto.addWindowListener(new java.awt.event.WindowAdapter() {
                @Override
                public void windowClosed(java.awt.event.WindowEvent windowEvent) {
                    // Chama o método para atualizar a lista de alunos
                    addLista(academia.getEstoque());
                }
            });

            // Exibe a janela de edição
            editarProduto.setVisible(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = jTableProdutosCadastrados.getSelectedRow();
        
        if(linhaSelecionada == -1){
            JOptionPane.showMessageDialog(null, "Você não selecionou nenhum produto");
        }else{
            int resposta = JOptionPane.showConfirmDialog(
                    null,
                    "Tem certeza que deseja remover o produto?",
                    "Confirmar Remoção",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE
            );
            
            if(resposta == JOptionPane.YES_OPTION){
                controller.removerProduto(jTableProdutosCadastrados.getValueAt(linhaSelecionada, 1).toString());
                addLista(academia.getEstoque());
            }else{
                JOptionPane.showMessageDialog(null, "Não foi possível remover o produto!");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

