    /**
     * Método que formata a lista de funcionários para a tabela
     * @param list
     */
    public void addLista(ArrayList<Produto> list){
        DefaultTableModel model = (DefaultTableModel) getjTableProdutosCadastrados().getModel();
        model.setRowCount(0);

        for (Produto produto : list) {
            Object rowData[] = new Object[6];
            rowData[0] = produto.getId();
            rowData[1] = produto.getNome();
            rowData[2] = produto.getDescricao();
            rowData[3] = produto.getQuantidade();
            rowData[4] = produto.getValorCompra();
            rowData[5] = produto.getValorVenda();
            model.addRow(rowData);
        }
    }
    
