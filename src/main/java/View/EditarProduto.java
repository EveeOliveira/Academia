    private void jButton1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jButton1KeyPressed
        // TODO add your handling code here:
        /**
         * Cadastra aluno com o ENTER
         */
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            int quantidadeA = Integer.parseInt(jTextQuantidade.getText());
            float valorCompraA = Float.parseFloat(jTextVCompra.getText());
            float valorVendaA = Float.parseFloat(jTextVVenda.getText());
            Produto produto = new Produto(
                    jTextNome.getText(), 
                    jTextDescricao.getText(),
                    quantidadeA,
                    valorCompraA,
                    valorVendaA
            );
            if(controller.editarProduto(jTextNome.getText(), produto)){
                JOptionPane.showMessageDialog(null, "Produto editado com sucesso!");
            } else{
                JOptionPane.showMessageDialog(null, "Não foi possivel editar o produto!");
            }
            dispose();
        }
    }//GEN-LAST:event_jButton1KeyPressed


